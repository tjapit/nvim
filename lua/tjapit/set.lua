-- set options
local opt = vim.opt

-- relative line number
opt.relativenumber = true
opt.nu = true

-- whitespace
opt.tabstop = 2
opt.softtabstop = 2
opt.shiftwidth = 2
opt.expandtab = true
opt.smarttab = true

opt.smartindent = true

opt.wrap = true

-- search
opt.hlsearch = false
opt.incsearch = true

-- Neovim UI
opt.termguicolors = true
opt.scrolloff = 20
opt.updatetime = 50
opt.colorcolumn = "80"
opt.ignorecase = true
opt.smartcase = true

